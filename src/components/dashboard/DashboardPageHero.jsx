function PageHeroArt({ variant }) {
  switch (variant) {
    case "roleplay":
      return <HeroArtRoleplay />;
    case "metrics":
      return <HeroArtMetrics />;
    case "scorecards":
      return <HeroArtScorecards />;
    case "team":
      return <HeroArtTeam />;
    case "overview":
    default:
      return <HeroArtOverview />;
  }
}

/**
 * @param {object} props
 * @param {string} props.eyebrow
 * @param {string} props.title
 * @param {import('react').ReactNode} props.subtitle
 * @param {{ href: string, label: string, onClick?: (e: import('react').MouseEvent) => void }} [props.helpLink]
 * @param {{ label: string, onClick?: () => void, icon?: import('react').ReactNode, trailingPlus?: boolean, disabled?: boolean }} [props.primaryButton]
 * @param {import('react').ReactNode} [props.secondaryAction]
 * @param {'overview'|'metrics'|'scorecards'|'team'|'roleplay'} [props.variant]
 */
export default function DashboardPageHero({
  eyebrow,
  title,
  subtitle,
  helpLink,
  primaryButton,
  secondaryAction,
  variant = "overview",
}) {
  return (
    <header className="db-page-hero">
      <div className="db-page-hero-content">
        <span className="db-page-hero-label">{eyebrow}</span>
        <h1 className="db-page-title">{title}</h1>
        <div className="db-page-subtitle">{subtitle}</div>
        {helpLink ? (
          <a
            href={helpLink.href}
            className="db-page-help-link"
            onClick={helpLink.onClick}
          >
            {helpLink.label}
          </a>
        ) : null}
      </div>
      <div className="db-page-hero-right">
        <PageHeroArt variant={variant} />
        {(secondaryAction || primaryButton) ? (
          <div className="db-page-hero-actions">
            {secondaryAction}
            {primaryButton ? (
              <button
                type="button"
                className="db-page-primary-btn"
                onClick={primaryButton.onClick}
                disabled={primaryButton.disabled}
              >
                {primaryButton.icon}
                {primaryButton.label}
                {primaryButton.trailingPlus ? <span aria-hidden="true"> +</span> : null}
              </button>
            ) : null}
          </div>
        ) : null}
      </div>
    </header>
  );
}
